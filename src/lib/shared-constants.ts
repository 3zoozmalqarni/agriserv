export function getSaudiDate(): string {
  const now = new Date();
  const saudiTime = new Date(now.toLocaleString('en-US', { timeZone: 'Asia/Riyadh' }));
  const year = saudiTime.getFullYear();
  const month = String(saudiTime.getMonth() + 1).padStart(2, '0');
  const day = String(saudiTime.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

export function getSaudiDateTime(): string {
  const now = new Date();
  return now.toLocaleString('en-US', {
    timeZone: 'Asia/Riyadh',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit',
    hour12: false
  }).replace(/(\d+)\/(\d+)\/(\d+),?\s*(\d+):(\d+):(\d+)/, '$3-$1-$2T$4:$5:$6');
}
